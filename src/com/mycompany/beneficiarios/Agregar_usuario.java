    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String usuario = txt_user.getText().trim();
        String contrasena = new String(txt_password.getPassword());
        String contrasenaConfirmada = new String(txt_password_confirm.getPassword());
        String correo = txt_correo.getText();

        // Validar si las contrase√±as coinciden
        if (!contrasena.equals(contrasenaConfirmada)) {
            JOptionPane.showMessageDialog(null, "Las contrase√±as no coinciden", "Error", JOptionPane.ERROR_MESSAGE);
            txt_password_confirm.setText("");
            return;
        }

        // Validar si el correo es v√°lido
        if (!validarCorreo(correo)) {
            JOptionPane.showMessageDialog(null, "Ingrese un correo electr√≥nico v√°lido", "Error", JOptionPane.ERROR_MESSAGE);
            txt_correo.setText("");
            return;
        }
        
        boolean usuarioAgregado = Usuario.agregarUsuario(usuario, correo, contrasena);

        if (usuarioAgregado) {
            JOptionPane.showMessageDialog(null, "Usuario agregado exitosamente: " + usuario, "√âxito", JOptionPane.INFORMATION_MESSAGE);
            
            Iniciar_sesion i = new Iniciar_sesion();
            i.setVisible(true);
            dispose();
        } else {
            JOptionPane.showMessageDialog(null, "Error al agregar usuario: " + usuario, "Error", JOptionPane.ERROR_MESSAGE);
            txt_user.setText("");
            txt_password.setText("");
            txt_password_confirm.setText("");
            txt_correo.setText("");
        }

    }//GEN-LAST:event_jButton1ActionPerformed

     public static boolean validarCorreo(String correo) {
        // Patr√≥n de expresi√≥n regular para validar un correo electr√≥nico
        String patronCorreo = "^[a-zA-Z0-9_+&*-]+(?:\\.[a-zA-Z0-9_+&*-]+)*@(?:[a-zA-Z0-9-]+\\.)+[a-zA-Z]{2,7}$";
        return Pattern.compile(patronCorreo).matcher(correo).matches();
    }

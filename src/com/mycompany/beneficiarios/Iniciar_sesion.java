    private void btn_passwordActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_passwordActionPerformed
        Container c = getContentPane();
        if (btn_password.isSelected()) {
            txt_password.setEchoChar((char) 0);
            
            ImageIcon iconOcultar = new ImageIcon("src/main/java/com/mycompany/beneficiarios/imagenes/ocultar.png");
            btn_password.setIcon(iconOcultar);
        } else {
            ImageIcon iconMostrar = new ImageIcon("src/main/java/com/mycompany/beneficiarios/imagenes/mostrar.png");
            btn_password.setIcon(iconMostrar);
            txt_password.setEchoChar('*');
        }
    }//GEN-LAST:event_btn_passwordActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String nombreUsuario = txt_user.getText().trim();
        char[] contrasena_c = txt_password.getPassword();
        String contrasena = new String(contrasena_c);

        // Intentar iniciar sesi칩n
        boolean inicioSesionExitoso = Usuario.iniciarSesion(nombreUsuario, contrasena);

        if (inicioSesionExitoso) {
            System.out.println("Inicio de sesi칩n exitoso para el usuario: " + nombreUsuario);
            Menu m = new Menu();
            m.setUsuario(nombreUsuario);
            m.setVisible(true);
            dispose();
        } else {
            System.out.println("Inicio de sesi칩n fallido para el usuario: " + nombreUsuario);
            txt_user.setText("");
            txt_password.setText("");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

